if ($.cookie('lang') != null) {
    translateFunc($.cookie('lang'));
} else {
    $.cookie('lang', 'BR');
    translateFunc('BR');
}

$('header .navbar-nav .change-lang .current-lang').click(function () {
    $('header .navbar-nav .change-lang .dropdown-lang').toggleClass('show');
});

$('header .change-lang .dropdown-lang .select-lang').click(function () {
    $('header .change-lang .dropdown-lang .select-lang').removeClass('active');
    $(this).addClass('active');
    $.removeCookie('lang');
    $.cookie('lang', $(this).data('id'));

    if ($('body').find('section#product-wrapper').hasClass('politic')) {
        if ($.cookie('lang') == 'BR') window.location.href = '/politic.html';
        else window.location.href = '/politic';

        return;
    }

    translateFunc($(this).data('id'));

    $('header .navbar-nav .change-lang .current-lang p').text($(this).data('id'));
    $('header .navbar-nav .change-lang .dropdown-lang').removeClass('show');


});

function translateFunc(lang) {
    $('header .change-lang .dropdown-lang .select-lang').removeClass('active');

    if (lang == 'BR') {
        $('header .change-lang .dropdown-lang .select-lang[data-id="BR"]').addClass('active');
        $('header .navbar-nav .change-lang .current-lang p').text('BR');

        $('footer .mb-change').attr('href', '/politic.html');

        $('#products .search-block .search input').attr('placeholder', 'ProcurarJogosы');

        $('body .change-price-mb-dollar').each(function () {
            $(this).text($(this).data('ruble') + 'R$');
        });

        $('#product .main-info .accept-order .go-buy a.btn-new-style').attr('href', $('#product .main-info .accept-order .select .select-sub .sel-wrapper .sel.active').data('link'));

        $('#product .content .system-recom-block .functions-wrapper-block .desc b').each(function () {
            if ($(this).data('BR') != '')
                $(this).text($(this).data('BR'));
        });

        for (let i = 0; i < translate.length; i++) {
            $('body :not([data-chlang-ru], [data-chlang-en], script, style, #products.cheats .count-products, .functions-wrapper-block .desc b, .right-block .desc, .warranty-card p, #product-wrapper.politic .-navigation-links a, #product-wrapper.politic .terms, #product-wrapper.politic .terms p)').contents().filter(function () {
                return this.nodeType === 3;
            }).replaceWith(function () {
                return this.nodeValue.replace(translate[i][1], translate[i][0]);
            });
        }

        $('body *[data-chlang-ru]').each(function () {
            $(this).text($(this).data('chlang-ru'));
        });
    } else {
        $('header .change-lang .dropdown-lang .select-lang[data-id="EN"]').addClass('active');
        $('header .navbar-nav .change-lang .current-lang p').text('EN');

        $('footer .mb-change').attr('href', '/politic');

        $('#products .search-block .search input').attr('placeholder', 'Search game');

        $('body .change-price-mb-dollar').each(function () {
            $(this).text($(this).data('dollar') + '$');
        });

        if ($('#product .main-info .accept-order').hasClass('multi-region')) {
            $('#product .main-info .accept-order .go-buy a.btn-new-style').attr('href', $('#product .main-info .accept-order .select .select-sub .sel-wrapper .sel.active').data('link_other'));
        }

        $('#product .content .system-recom-block .functions-wrapper-block .desc b').each(function () {
            if ($(this).data('eng') != '')
                $(this).text($(this).data('eng'));
        });

        for (let i = 0; i < translate.length; i++) {
            $('body :not([data-chlang-ru], [data-chlang-en], script, style, #products.cheats .count-products, .functions-wrapper-block .desc b, .right-block .desc, .warranty-card p, #product-wrapper.politic .-navigation-links a, #product-wrapper.politic .terms, #product-wrapper.politic .terms p)').contents().filter(function () {
                return this.nodeType === 3;

            }).replaceWith(function () {
                return this.nodeValue.replace(translate[i][0], translate[i][1]);
            });
        }

        $('body *[data-chlang-en]').each(function () {
            $(this).text($(this).data('chlang-en'));
        });
    }
}
